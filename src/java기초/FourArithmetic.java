package java기초;

public class FourArithmetic {
    public static void main(String[] args) {
        // 두 개의 정수를 사칙연산(+,-,*,/)하여 출력하는 JavaSE프로그램을 만들어보자
        int a=12;
        int b=2;
        System.out.printf("a+b=%d%n",a+b);
        // System.out.println("a+b="+(a+b));
        System.out.printf("a-b=%d%n",a-b);
        System.out.printf("a*b=%d%n",a*b);
        System.out.printf("a/b=%d%n",a/b);
    }
}
